interface ActionCardProps {
  item: ActionItem;
  isPersonal?: boolean;
  onStatusUpdate?: (status: ActionItem['status'], snoozeUntil?: string) => void;
}

const ActionCard: React.FC<ActionCardProps> = ({ item, isPersonal, onStatusUpdate }) => {
  const isSnoozed = item.snoozed_until && new Date(item.snoozed_until) > new Date();
  const snoozeText = isSnoozed ? `Snoozed until ${new Date(item.snoozed_until!).toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' })}` : null;

  return (
    <div className={`p-6 rounded-3xl border transition-all hover:scale-[1.01] hover:shadow-xl group relative overflow-hidden 
      ${isPersonal ? 'bg-white border-rose-100 shadow-rose-200/20' : 'bg-slate-50 border-slate-100 hover:bg-white'}
      ${isSnoozed ? 'opacity-50 grayscale' : ''}`}>
      
      {isPersonal && <div className="absolute left-0 top-0 w-1.5 h-full bg-rose-500"></div>}
      <div className="flex flex-col sm:flex-row justify-between items-start sm:items-center gap-4">
        <div className="space-y-2 flex-1">
          <div className="flex items-center space-x-3">
            <span className={`px-2 py-0.5 rounded text-[8px] font-black uppercase tracking-widest border transition-colors ${
              item.status === 'COMPLETED' ? 'text-emerald-500 bg-emerald-50 border-emerald-100' : 
              isSnoozed ? 'text-amber-600 bg-amber-50 border-amber-100' :
              'text-slate-400 bg-white border-slate-100'
            }`}>
              {isSnoozed ? 'SNOOZED' : item.status}
            </span>
            <span className={`px-2 py-0.5 rounded text-[8px] font-black uppercase tracking-widest border border-slate-100 bg-white text-slate-500`}>
              {item.priority} Priority
            </span>
            {!isPersonal && item.owner && (
               <span className="text-[8px] font-black uppercase tracking-widest text-indigo-600">Assigned: {item.owner}</span>
            )}
          </div>
          <p className={`text-lg font-extrabold tracking-tight ${item.status === 'COMPLETED' ? 'line-through text-slate-300' : isPersonal ? 'text-slate-900' : 'text-slate-700'}`}>{item.task}</p>
          <div className="flex flex-wrap items-center gap-4 text-[10px] font-black text-slate-400 uppercase tracking-widest">
             <span className="flex items-center"><svg className="w-3.5 h-3.5 mr-1" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path d="M8 7V3m8 4V3m-9 8h10M5 21h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2-2v12a2 2 0 002 2z" strokeWidth="2"/></svg>Due {item.deadline}</span>
             {snoozeText && <span className="flex items-center text-amber-600 italic">üåô {snoozeText}</span>}
          </div>
        </div>
        <div className="flex items-center gap-2 opacity-0 group-hover:opacity-100 transition-opacity no-print">
           {item.status !== 'COMPLETED' ? (
             <button 
               onClick={() => onStatusUpdate?.('COMPLETED')}
               className="px-4 py-2 bg-emerald-500 text-white rounded-xl text-[10px] font-black uppercase tracking-widest hover:bg-emerald-600 shadow-lg shadow-emerald-200"
             >
               Complete
             </button>
           ) : (
             <button 
               onClick={() => onStatusUpdate?.('PENDING')}
               className="px-4 py-2 bg-slate-200 text-slate-600 rounded-xl text-[10px] font-black uppercase tracking-widest hover:bg-slate-300"
             >
               Undo
             </button>
           )}
           <button 
             onClick={() => {
               const snoozeDate = new Date();
               snoozeDate.setHours(snoozeDate.getHours() + 24);
               onStatusUpdate?.('PENDING', snoozeDate.toISOString());
             }}
             className="px-4 py-2 bg-slate-100 text-slate-500 rounded-xl text-[10px] font-black uppercase tracking-widest hover:bg-slate-200"
           >
             Snooze 24h
           </button>
        </div>
      </div>
    </div>
  );
};

